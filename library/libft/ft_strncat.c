#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	k;
	int		i;

	i = 0;
	k = 0;
	if (n == 0)
		return (s1);
	while (s1[i] != '\0')
		i++;
	while (k < n && s2[k] != '\0')
	{
		s1[i] = s2[k];
		i++;
		k++;
	}
	s1[i] = '\0';
	return (s1);
}
